function matchesReadingQueueEntry(entry, properties) {
    if (!entry || !properties) return false;
    const iso = window.resolveCountryIso(properties);
    if (entry.iso && iso === entry.iso) return true;
    return Boolean(entry.country && properties.ADMIN === entry.country);
}

window.getOnTheDocketBooks = function getOnTheDocketBooks(properties) {
    if (!properties || !window.ON_THE_DOCKET) return [];
    return window.ON_THE_DOCKET.filter((entry) => matchesReadingQueueEntry(entry, properties));
};

window.getOnTheDocketBook = function getOnTheDocketBook(properties) {
    const books = window.getOnTheDocketBooks(properties);
    return books[0] || null;
};

window.isOnTheDocketCountry = function isOnTheDocketCountry(properties) {
    return window.getOnTheDocketBooks(properties).length > 0;
};

window.resolveCountryIso = function resolveCountryIso(properties) {
    if (!properties) return null;
    if (properties.ISO_A2 && properties.ISO_A2 !== '-99') return properties.ISO_A2;
    if (properties.ISO_A2_EH && properties.ISO_A2_EH !== '-99') return properties.ISO_A2_EH;
    if (properties.ADM0_A3 && properties.ADM0_A3 !== '-99') return properties.ADM0_A3;
    return null;
};

const BOOK_ISO_ALIASES = {
    FRA: 'FR',
    ITA: 'IT',
    USA: 'US',
    GBR: 'GB',
    KOR: 'KR'
};

function booksEntryForKey(key) {
    if (!key) return null;
    const entry = window.BOOKS_BY_COUNTRY[key];
    if (!entry) return null;
    return Array.isArray(entry) ? entry : [entry];
}

window.getBooksForCountry = function getBooksForCountry(properties) {
    if (!properties) return [];
    const iso = window.resolveCountryIso(properties);
    if (iso) {
        const byIso = booksEntryForKey(iso);
        if (byIso) return byIso;
        const alias = BOOK_ISO_ALIASES[iso];
        if (alias) return booksEntryForKey(alias) || [];
    }
    return booksEntryForKey(properties.ADMIN) || [];
};

window.lookupBookForCountry = function lookupBookForCountry(properties, index) {
    const books = window.getBooksForCountry(properties);
    if (books.length === 0) return null;
    const i = Number.isFinite(index) ? index : 0;
    return books[((i % books.length) + books.length) % books.length];
};

window.countryHasBook = function countryHasBook(properties) {
    return window.getBooksForCountry(properties).length > 0;
};
